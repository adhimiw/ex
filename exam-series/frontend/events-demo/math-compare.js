const value = 9.41;

console.log("Input:", value);
console.log("Math.round(value):", Math.round(value), "// nearest integer");
console.log("Math.ceil(value):", Math.ceil(value), "// always upward to next integer");
console.log("Math.floor(value):", Math.floor(value), "// always downward to previous integer");

// Extra example near midpoint
const midpoint = 9.5;
console.log("Input:", midpoint);
console.log("Math.round(9.5):", Math.round(midpoint));
console.log("Math.ceil(9.5):", Math.ceil(midpoint));
console.log("Math.floor(9.5):", Math.floor(midpoint));
